    fun initCache() {
        if (cacheReady) return

        val entries = GLYPHS.entries
        val iterator = entries.iterator()
        while (iterator.hasNext()) {
            val entry = iterator.next()
            glyphCache[entry.key.code] = entry.value
        }
        ShinonomeGeneratedGlyphs.populate(glyphCache)

        val questionGlyph = glyphCache['?'.code]
        if (questionGlyph != null) {
            missingGlyph = questionGlyph
        }

        cacheReady = true
    }

    fun glyphFor(char: Char): IntArray {
        initCache()

        val charCode = char.code
        val cachedGlyph = glyphCache[charCode]
        if (cachedGlyph != null) {
            return cachedGlyph
        }

        val provider = fallbackProvider
        if (provider != null) {
            val fallbackGlyph = provider.invoke(char)
            glyphCache[charCode] = fallbackGlyph
            return fallbackGlyph
        }

        glyphCache[charCode] = missingGlyph
        return missingGlyph
    }

    fun hasGlyph(char: Char): Boolean {
        initCache()
        return GLYPHS.containsKey(char) || ShinonomeGeneratedGlyphs.hasGeneratedGlyph(char)
    }
